def addition(x, y):
    """
        x: int
        y: int
    """
    return x + y


def subtraction(x, y):
    """
        x: int
        y: int
    """
    return x - y


def multiplication(x, y):
    """
        x: int
        y: int
    """
    return x * y


def division(x, y):
    """
        x: int
        y: int
    """
    return x / y
